namespace OperadoresDeAtribuicao {
    class OperadoresDeAtribuicao {
        static void Main(string[] args) {
            int a = 10;
            Console.WriteLine(a);

            a += 2;
            Console.WriteLine(a);

            a *= 3;
            Console.WriteLine(a);
            Console.WriteLine("--------------------");

            string s = "ABC";
            Console.WriteLine(s);

            s += "DEF";
            Console.WriteLine(s);
        }
    }
}